def base_visual(visual_type, x, y, width, height, title=None,
                z=1000, title_size="14pt", title_align="left"):
    v = {
        "$schema": "https://developer.microsoft.com/json-schemas/fabric/item/report/definition/visualContainer/2.6.0/schema.json",
        "name": gid(),
        "position": {"x": x, "y": y, "z": z,
                     "width": width, "height": height, "tabOrder": z},
        "visual": {
            "visualType": visual_type,
            "visualContainerObjects": {
                "border": [{"properties": {"show": {"expr": {"Literal": {"Value": "false"}}}}}],
                "background": [{"properties": {
                    "color": {"solid": {"color": {"expr": {"Literal": {"Value": f"'{WHITE}'"}}}}},
                    "show": {"expr": {"Literal": {"Value": "true"}}},
                    "transparency": {"expr": {"Literal": {"Value": "0D"}}}
                }}],
            },
        },
    }
    if title:
        # Keep title styling minimal — let the theme drive colour/font.
        v["visual"]["visualContainerObjects"]["title"] = [{
            "properties": {
                "show": {"expr": {"Literal": {"Value": "true"}}},
                "text": {"expr": {"Literal": {"Value": f"'{title}'"}}}
            }
        }]
    else:
        v["visual"]["visualContainerObjects"]["title"] = [{
            "properties": {"show": {"expr": {"Literal": {"Value": "false"}}}}
        }]
    return v


def proj(table, col_or_measure, is_measure=False, active=False):
    if is_measure:
        field = {"Measure": {
            "Expression": {"SourceRef": {"Entity": table}},
            "Property": col_or_measure
        }}
    else:
        field = {"Column": {
            "Expression": {"SourceRef": {"Entity": table}},
            "Property": col_or_measure
        }}
    p = {"field": field,
         "queryRef": f"{table}.{col_or_measure}",
         "nativeQueryRef": col_or_measure}
    if active:
        p["active"] = True
    return p


def card(table, measure, x, y, w, h, title=None):
    v = base_visual("card", x, y, w, h, title)
    v["visual"]["query"] = {"queryState": {
        "Values": {"projections": [proj(table, measure, is_measure=True, active=True)]}
    }}
    return v


