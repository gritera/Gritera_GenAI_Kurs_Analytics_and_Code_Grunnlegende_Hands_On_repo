def table_visual(table, columns, x, y, w, h, title=None, measures=None):
    """columns: list of plain column names. measures: list of (table, measure_name) tuples."""
    v = base_visual("tableEx", x, y, w, h, title)
    projections = [proj(table, c) for c in columns]
    for m in (measures or []):
        projections.append(proj(m[0], m[1], is_measure=True, active=True))
    v["visual"]["query"] = {"queryState": {
        "Values": {"projections": projections}
    }}
    return v


