def gid():
    """
    Deterministisk 20-tegns id basert på (page, counter).
    Sikrer at gjenoppbygging skriver over samme filer — viktig på mounted
    filsystemer der vi ikke kan slette.
    """
    page = _CURRENT_PAGE[0] or "global"
    n = _VISUAL_COUNTERS.get(page, 0)
    _VISUAL_COUNTERS[page] = n + 1
    # 20-char hex name, encodes page+index deterministically
    import hashlib
    h = hashlib.md5(f"{page}::v{n:04d}".encode()).hexdigest()
    return h[:20]


def lineage_guid():
    """Generer en UUID-formatert lineage-tag (brukes av TMDL)."""
