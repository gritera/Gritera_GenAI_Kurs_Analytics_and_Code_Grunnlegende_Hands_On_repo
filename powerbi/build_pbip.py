    def_dir = os.path.join(DATASET_DIR, "definition")

    # database.tmdl
    qorder = '","'.join(TABLES.keys())
    write_text(os.path.join(def_dir, "database.tmdl"),
        f"database {PROJECT_NAME}\n"
        f"\tcompatibilityLevel: 1567\n\n"
        f'\tannotation PBI_QueryOrder = ["{qorder}"]\n\n'
        f"\tannotation __PBI_TimeIntelligenceEnabled = 0\n"
    )

    # Generate stable relationship IDs so rebuilds don't churn lineageTags
    # Stable by hashing the (from, to) column pair.
    import hashlib
    rel_ids = []
    rel_lines = []
    for rel in RELATIONSHIPS:
        from_t, from_c, to_t, to_c, behaviour = rel
        sig = f"{from_t}.{from_c}=>{to_t}.{to_c}"
        h = hashlib.md5(sig.encode()).hexdigest()
        rid = f"{h[0:8]}-{h[8:12]}-{h[12:16]}-{h[16:20]}-{h[20:32]}"
        rel_ids.append(rid)
        rel_lines.append(f"relationship {rid}")
        rel_lines.append(f"\tfromColumn: {from_t}.{from_c}")
        rel_lines.append(f"\ttoColumn: {to_t}.{to_c}")
        if behaviour == "bothDirections":
            rel_lines.append("\tcrossFilteringBehavior: bothDirections")
        rel_lines.append("")

    # Put authoritative relationship blocks in relationships.tmdl
    write_text(os.path.join(def_dir, "relationships.tmdl"), "\n".join(rel_lines))

    # model.tmdl: refs only — ref table, ref cultureInfo, ref relationship
    table_refs = "\n".join(f"ref table {t}" for t in TABLES.keys())
    rel_refs = "\n".join(f"ref relationship {rid}" for rid in rel_ids)

    model_tmdl = (
        f"model Model\n"
        f"\tculture: nb-NO\n"
        f"\tdefaultPowerBIDataSourceVersion: powerBI_V3\n"
        f"\tsourceQueryCulture: nb-NO\n"
        f"\tdataAccessOptions\n"
        f"\t\tlegacyRedirects\n"
        f"\t\treturnErrorValuesAsNull\n"
        f"\n"
        f"\tannotation PBIDesktopVersion = 2.131.1203.0 (24.12)\n"
        f'\tannotation PBI_QueryOrder = ["{qorder}"]\n'
        f"\n"
        f"{table_refs}\n"
        f"\n"
        f"ref cultureInfo nb-NO\n"
        f"\n"
        f"{rel_refs}\n"
    )
    write_text(os.path.join(def_dir, "model.tmdl"), model_tmdl)

    # expressions.tmdl — RepoPath parameter
    expressions = (
        "expression RepoPath = \"" + DEFAULT_CSV_FOLDER + "\" meta [IsParameterQuery=true, "
        "Type=\"Text\", IsParameterQueryRequired=true]\n"
        "\tlineageTag: " + lineage_guid() + "\n"
        "\tqueryGroup: Parameters\n"
        "\n"
        "\tannotation PBI_ResultType = Text\n"
    )
    write_text(os.path.join(def_dir, "expressions.tmdl"), expressions)

    # culture.tmdl
    write_text(os.path.join(def_dir, "cultures", "nb-NO.tmdl"),
        "cultureInfo nb-NO\n"
    )

    # tables
    for table_name, spec in TABLES.items():
        path = os.path.join(def_dir, "tables", f"{table_name}.tmdl")
        write_text(path, render_table_tmdl(table_name, spec))

    # definition.pbism — semantic model entry point (modern PBIP)
    write_json(os.path.join(DATASET_DIR, "definition.pbism"), {
        "version": "1.0",
        "settings": {}
    })


