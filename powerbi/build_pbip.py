def neutralize_stale_visuals():
    """
    On mounted folders we can WRITE but not DELETE files. So for any visual.json
    left over from a previous build (random folder name we did not just generate),
    overwrite it with a 1×1 off-canvas hidden shape so Power BI ignores it.
    """
    pages_dir = os.path.join(REPORT_DIR, "definition", "pages")
    if not os.path.isdir(pages_dir):
        return
    for page in os.listdir(pages_dir):
        visuals_dir = os.path.join(pages_dir, page, "visuals")
        if not os.path.isdir(visuals_dir):
            continue
        live = BUILT_VISUALS.get(page, set())
        for folder in os.listdir(visuals_dir):
            if folder in live:
                continue
            stale_path = os.path.join(visuals_dir, folder, "visual.json")
            if not os.path.isfile(stale_path):
                continue
            # Overwrite with a 1px hidden shape off the canvas
            noop = {
                "$schema": "https://developer.microsoft.com/json-schemas/fabric/item/report/definition/visualContainer/2.6.0/schema.json",
                "name": folder,
                "position": {"x": -10000, "y": -10000, "z": 0,
                             "width": 1, "height": 1, "tabOrder": 0},
                "visual": {
                    "visualType": "shape",
                    "isHidden": True,
                    "objects": {
                        "shape": [{"properties": {
                            "tileShape": {"expr": {"Literal": {"Value": "'rectangle'"}}}
                        }}],
                        "fill": [{"properties": {
                            "fillColor": {"solid": {"color": {"expr": {"Literal": {"Value": "'#FFFFFF'"}}}}},
                            "transparency": {"expr": {"Literal": {"Value": "100D"}}}
                        }, "selector": {"id": "default"}}],
                        "outline": [{"properties": {
                            "show": {"expr": {"Literal": {"Value": "false"}}}
                        }}]
                    },
                    "drillFilterOtherVisuals": True
                }
            }
            try:
                with open(stale_path, "w", encoding="utf-8") as f:
                    json.dump(noop, f)
            except OSError:
                pass


