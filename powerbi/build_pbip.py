def _rm_tree_safe(path):
    """Walk and delete files/dirs individually — robust on mounted folders."""
    if not os.path.isdir(path):
        return
    for root, dirs, files in os.walk(path, topdown=False):
        for f in files:
            try:
                os.remove(os.path.join(root, f))
            except OSError:
                pass
        for d in dirs:
            try:
                os.rmdir(os.path.join(root, d))
            except OSError:
                pass
    try:
        os.rmdir(path)
    except OSError:
        pass





def main():
    # Clean previous build artifacts (keep CSV data & build script intact)
    legacy_dataset = os.path.join(ROOT, f"{PROJECT_NAME}.Dataset")
    for d in (DATASET_DIR, REPORT_DIR, legacy_dataset):
        _rm_tree_safe(d)
    for f in os.listdir(ROOT):
        if f.endswith(".pbip"):
            try:
                os.remove(os.path.join(ROOT, f))
            except OSError:
                pass

    print(f"Building {PROJECT_NAME} ...")
    build_pbip_entry()
    build_dataset()
    build_report()
    neutralize_stale_visuals()
    print(f"Done. Open powerbi/{PROJECT_NAME}.pbip in Power BI Desktop.")
    print()
    print("Tip: open the model in Power BI Desktop and make sure the")
    print("'RepoPath' parameter points to the absolute path of powerbi/data.")


