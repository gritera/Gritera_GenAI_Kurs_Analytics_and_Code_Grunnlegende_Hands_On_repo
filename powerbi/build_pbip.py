DATA_TYPE_PBI = {
    "int64":    "int64",
    "double":   "double",
    "string":   "string",
    "dateTime": "dateTime",
    "boolean":  "boolean",
}


def tmdl_column(name, dtype, summarize_by, format_string,
                is_hidden=False, sort_by_column=None,
                data_category=None):
    lines = [f"\tcolumn {name}"]
    lines.append(f"\t\tdataType: {DATA_TYPE_PBI[dtype]}")
    if format_string:
        lines.append(f"\t\tformatString: {format_string}")
    lines.append(f"\t\tlineageTag: {lineage_guid()}")
    lines.append(f"\t\tsummarizeBy: {summarize_by}")
    lines.append(f"\t\tsourceColumn: {name}")
    if is_hidden:
        lines.append("\t\tisHidden")
    if sort_by_column:
        lines.append(f"\t\tsortByColumn: {sort_by_column}")
    if data_category:
        lines.append(f"\t\tdataCategory: {data_category}")
    lines.append("")
    lines.append("\t\tannotation SummarizationSetBy = Automatic")
    if dtype == "dateTime":
        lines.append("")
        lines.append("\t\tannotation UnderlyingDateTimeDataType = Date")
    return "\n".join(lines)


def tmdl_measure(name, expression, format_string, display_folder):
    """Render a TMDL measure block. Multi-line DAX is supported."""
    if "\n" in expression:
        # Multi-line DAX: use ``` fenced block
        expr = "```\n\t\t\t" + expression.replace("\n", "\n\t\t\t") + "\n\t\t\t```"
        head = f"\tmeasure '{name}' =\n\t\t{expr}"
    else:
        head = f"\tmeasure '{name}' = {expression}"
    parts = [head]
    if format_string:
        parts.append(f"\t\tformatString: {json.dumps(format_string)}")
    parts.append(f"\t\tlineageTag: {lineage_guid()}")
    if display_folder:
        parts.append(f"\t\tdisplayFolder: {display_folder}")
    parts.append("")
    parts.append("\t\tannotation PBI_FormatHint = {\"isGeneralNumber\":true}")
    return "\n".join(parts)


def tmdl_partition_csv(table_name, csv_filename):
    """
    Power Query M som leser en CSV-fil fra repo-mappen via parameter `RepoPath`.
    Mode = import.
    """
    m = (
        "let\n"
        f"\tSource = Csv.Document(\n"
        f"\t\tFile.Contents(RepoPath & \"/{csv_filename}\"),\n"
        "\t\t[Delimiter=\",\", Encoding=65001, QuoteStyle=QuoteStyle.Csv]),\n"
        "\tPromoted = Table.PromoteHeaders(Source, [PromoteAllScalars=true]),\n"
        f"\tTyped = {table_name}_Types(Promoted)\n"
        "in\n"
        "\tTyped"
    )
    expr = "```\n\t\t\t" + m.replace("\n", "\n\t\t\t") + "\n\t\t\t```"
    return (
        f"\tpartition {table_name} = m\n"
        f"\t\tmode: import\n"
        f"\t\tsource =\n"
        f"\t\t\t{expr}"
    )


def build_csv_partition_simple(table_name, csv_filename, columns):
    """
    Selvstendig M-uttrykk som ikke trenger en separat type-funksjon.
    Bygger en Table.TransformColumnTypes-liste fra column-spec.
    """
    type_map = {
        "int64":    "Int64.Type",
        "double":   "type number",
        "string":   "type text",
        "dateTime": "type date",
        "boolean":  "type logical",
    }
    transform_pairs = []
    for col in columns:
        name, dtype = col[0], col[1]
        transform_pairs.append(f'{{"{name}", {type_map[dtype]}}}')
    transform_list = ", ".join(transform_pairs)

    m = (
        "let\n"
        f'\tSource = Csv.Document(File.Contents(RepoPath & "/{csv_filename}"),'
        f' [Delimiter=",", Columns={len(columns)}, Encoding=65001, QuoteStyle=QuoteStyle.Csv]),\n'
        "\tPromoted = Table.PromoteHeaders(Source, [PromoteAllScalars=true]),\n"
        f"\tTyped = Table.TransformColumnTypes(Promoted, {{{transform_list}}})\n"
        "in\n"
        "\tTyped"
    )
    return m


def render_table_tmdl(table_name, spec):
    cols = spec["columns"]
    hidden = spec.get("hidden_columns", set())
    sort_map = spec.get("sort_columns", {})
    data_category = spec.get("data_category")
    is_date_table = spec.get("is_date_table", False)

    out = [f"table {table_name}"]
    out.append(f"\tlineageTag: {lineage_guid()}")
    if is_date_table:
        out.append("\tdataCategory: Time")
    out.append("")

    for col in cols:
        name, dtype, summarize, fmt = col
        col_block = tmdl_column(
            name, dtype, summarize, fmt,
            is_hidden=(name in hidden),
            sort_by_column=sort_map.get(name),
            data_category="Time" if (is_date_table and name == "date") else None,
        )
        out.append(col_block)
        out.append("")

    # Measures
    for m_table, m_name, m_expr, m_fmt, m_folder in MEASURES:
        if m_table != table_name:
            continue
        out.append(tmdl_measure(m_name, m_expr, m_fmt, m_folder))
        out.append("")

    # Partition
    m_expr = build_csv_partition_simple(table_name, f"{table_name}.csv", cols)
    fenced = "```\n\t\t\t" + m_expr.replace("\n", "\n\t\t\t") + "\n\t\t\t```"
    out.append(f"\tpartition {table_name} = m")
    out.append("\t\tmode: import")
    out.append("\t\tsource =")
    out.append(f"\t\t\t{fenced}")
    out.append("")
    out.append("\tannotation PBI_ResultType = Table")
    return "\n".join(out) + "\n"


